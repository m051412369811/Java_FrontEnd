// 全局前置守衛
router.beforeEach(async (to, from, next) => {
    const publicPages = ['/auth/login', '/auth/error', '/auth/access', '/pages/notfound'];
    const authRequired = !publicPages.includes(to.path);

    if (!authRequired) {
        return next();
    }

    try {
        const res = await userInfo();
        if (res.success) {
            next();
        } else {
            next({ name: 'login' });
        }
    } catch (e) {
        next({ name: 'login' });
    }
});

